
class Quotation {
  final String number;
  final DateTime date;
  final String customerName;
  final String customerMobile;
  final int itemCount;
  final int quantity;
  final double price;

  Quotation({
    required this.number,
    required this.date,
    required this.customerName,
    required this.customerMobile,
    required this.itemCount,
    required this.quantity,
    required this.price,
  });
}
